<template>
  <div class="login-container">
    <el-form ref="loginForm" :model="loginForm" :rules="loginRules" class="login-form" autocomplete="on" label-position="left">

      <div class="title-container">
        <h3 class="title">后台登录 V1.0</h3>
      </div>

      <el-form-item prop="username">
        <span class="svg-container">
          <svg-icon icon-class="user" />
        </span>
        <el-input
          ref="username"
          v-model="loginForm.username"
          placeholder="Username"
          name="username"
          type="text"
          tabindex="1"
          autocomplete="on"
        />
      </el-form-item>

      <el-tooltip v-model="capsTooltip" content="Caps lock is On" placement="right" manual>
        <el-form-item prop="password">
          <span class="svg-container">
            <svg-icon icon-class="password" />
          </span>
          <el-input
            :key="passwordType"
            ref="password"
            v-model="loginForm.password"
            :type="passwordType"
            placeholder="Password"
            name="password"
            tabindex="2"
            autocomplete="on"
            @keyup.native="checkCapslock"
            @blur="capsTooltip = false"
            @keyup.enter.native="handleLogin"
          />
          <span class="show-pwd" @click="showPwd">
            <svg-icon :icon-class="passwordType === 'password' ? 'eye' : 'eye-open'" />
          </span>
        </el-form-item>
      </el-tooltip>

      <el-form-item prop="code" style="width: 60%;float: left;">
        <span class="svg-container">
          <svg-icon icon-class="validCode" />
        </span>
        <el-input
          ref="username"
          v-model="loginForm.code"
          placeholder="验证码"
          name="username"
          type="text"
          tabindex="3"
          autocomplete="off"
          style=" width: 75%;"
          @keyup.enter.native="handleLogin"
        />
      </el-form-item>
      <div class="login-code" style=" width: 38%;height: 48px;float: right;">
        <img style="height: 48px;width: 100%;" :src="codeUrl" @click="getCode">
      </div>

      <el-button :loading="loading" type="primary" style="width:100%;margin-bottom:30px;" @click.native.prevent="handleLogin">
        <span v-if="!loading">登 录</span>
        <span v-else>登 录 中...</span>
      </el-button>

      <el-button type="text" @click="dialogVisible = true">点击打开 Dialog</el-button>
      <ImgLibrary
        :dialog-visible="dialogVisible"
        @changeDialogVisible="changeDialogVisible"
      />

    </el-form>
  </div>
</template>

<script>
import './directives.js'
import { getCodeImg } from '@/api/common/login'
import { validUsername } from '@/utils/validate'
import SocialSign from './components/SocialSignin'
import ImgLibrary from '@/components/ImgLibrary'

export default {
  name: 'Login',
  components: { SocialSign, ImgLibrary },
  data() {
    return {
      dialogVisible: false,
      //  新做
      codeUrl: '',
      loginForm: {
        username: '1131191695@qq.com',
        password: '123456',
        uuid: '', // 验证码归属id
        code: '' // 验证码
      },
      loginRules: {
        username: [{ required: true, trigger: 'blur', validator: '请输入账号！' }],
        password: [{ required: true, trigger: 'blur', validator: '请输入密码！' }],
        code: [{ required: true, trigger: 'change', validator: '验证码不能为空' }]
      },
      passwordType: 'password',
      capsTooltip: false,
      loading: false,
      redirect: undefined,
      otherQuery: {}
    }
  },
  watch: {
    $route: {
      handler: function(route) {
        const query = route.query
        if (query) {
          this.redirect = query.redirect
          this.otherQuery = this.getOtherQuery(query)
        }
      },
      immediate: true
    }
  },
  created() {
    this.getCode() // 页面加载就调用请求一下验证码
    // window.addEventListener('storage', this.afterQRScan)
  },
  mounted() {
    if (this.loginForm.username === '') {
      this.$refs.username.focus()
    } else if (this.loginForm.password === '') {
      this.$refs.password.focus()
    }
  },
  destroyed() {
    // window.removeEventListener('storage', this.afterQRScan)
  },
  methods: {
    changeDialogVisible(val) {
      this.dialogVisible = false
      console.log(val)
    },
    checkCapslock(e) {
      const { key } = e
      this.capsTooltip = key && key.length === 1 && (key >= 'A' && key <= 'Z')
    },
    showPwd() {
      if (this.passwordType === 'password') {
        this.passwordType = ''
      } else {
        this.passwordType = 'password'
      }
      this.$nextTick(() => {
        this.$refs.password.focus()
      })
    },
    // 请求获取验证码
    getCode() {
      getCodeImg().then(res => {
        this.codeUrl = res.data.data
        this.loginForm.uuid = res.data.id
      })
    },
    handleLogin() {
      this.$refs.loginForm.validate(valid => {
        if (valid) {
          this.loading = true
          this.$store.dispatch('user/login', this.loginForm)
            .then(() => {
              this.$router.push({ path: this.redirect || '/', query: this.otherQuery })
              this.loading = false
            })
            .catch(() => {
              this.getCode()
              this.loading = false
            })
        } else {
          return false
        }
      })
    },
    getOtherQuery(query) {
      return Object.keys(query).reduce((acc, cur) => {
        if (cur !== 'redirect') {
          acc[cur] = query[cur]
        }
        return acc
      }, {})
    }
  }
}
</script>

